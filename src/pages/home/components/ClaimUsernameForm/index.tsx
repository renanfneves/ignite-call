import { Button, TextInput, Text } from '@ignite-ui/react'
import { ArrowRight } from 'phosphor-react'
import { useForm } from 'react-hook-form'
import { z } from 'zod'
import { zodResolver } from '@hookform/resolvers/zod'
import { useRouter } from 'next/router'
import { Form, FormAnnotation } from './styles'

const claimUsernameFormSchema = z.object({
  username: z
    .string()
    .min(3, { message: 'O usu치rio precisa ter pelo menos 3 letras' })
    // this regex test for just letters and hifens
    .regex(/^([a-z\\-]+)$/i, {
      message: 'O usu치rio pode ter apenas letras e hifens',
    })
    .transform((username) => username.toLowerCase()),
})

// it creates a type infering the typeof above schema
type ClaimUsernameFormData = z.infer<typeof claimUsernameFormSchema>

export function ClaimUsernameForm() {
  const {
    register,
    handleSubmit,
    formState: { errors, isSubmitting },
  } = useForm<ClaimUsernameFormData>({
    // it applies validations from zod schema to form on submit
    resolver: zodResolver(claimUsernameFormSchema),
  })

  const router = useRouter()

  async function handleClaimUsername(data: ClaimUsernameFormData) {
    const { username } = data

    router.push(`/register?username=${username}`)
  }

  return (
    <>
      {/* we always need to wrap our submit function into useForm handleSubmit */}
      <Form as="form" onSubmit={handleSubmit(handleClaimUsername)}>
        <TextInput
          size="sm"
          prefix="ignite.com/"
          placeholder="seu-usu치rio"
          // to setup any input or value to our form we need to register it using this function from useForm
          {...register('username')}
        />
        <Button size="sm" type="submit" disabled={isSubmitting}>
          Reservar
          <ArrowRight />
        </Button>
      </Form>

      <FormAnnotation>
        <Text size="sm">
          {errors?.username
            ? errors.username.message
            : 'Digite o nome do usu치rio desejado'}
        </Text>
      </FormAnnotation>
    </>
  )
}
